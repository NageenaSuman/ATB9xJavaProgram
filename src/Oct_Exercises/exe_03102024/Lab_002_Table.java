public class Lab_002_Table {

    public static void main(String[] args){
        System.out.println("The Table of 10 being executed");
        for (int e = 1; e<= 10; e++ ) {
            int f = e * 10;
            System.out.println("10 * " + e + " = "+ f);}
}
}
