public class Lab_027_EvenOdd {
    public static void main(String[] args) {
        for (int i =0; i<=50; i++) {
            if (i % 2 == 0) {
                System.out.println("Even");
            } else {
                System.out.println("Odd");
            }
        }
    }
}
